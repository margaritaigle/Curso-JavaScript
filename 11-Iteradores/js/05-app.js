let i = 10000;
// Se ejecuta el codigo MIENTRAS se cumpla x condicion
// a diferencia del while, el do while si o si se ejecuta UNA vez 
// aunque no cumpla la condicion y luego comprueba
do {
    console.log(`Numero ${i}`); // Imprime Numero 10000

    i++; // Incremento

} while ( i < 10 ); // Condicion